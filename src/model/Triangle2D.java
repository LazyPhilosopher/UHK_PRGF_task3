package model;

public class Triangle2D {
    public double a_x, a_y,b_x,b_y,c_x,c_y;

    public Triangle2D(double _a_x, double _a_y, double _b_x, double _b_y, double _c_x, double _c_y){
        this.a_x = _a_x;
        this.a_y = _a_y;
        this.b_x = _b_x;
        this.b_y = _b_y;
        this.c_x = _c_x;
        this.c_y = _c_y;
    }
}
